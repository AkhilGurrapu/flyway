USE SCHEMA DEMO;

CREATE OR REPLACE TABLE DEMO.DEPARTMENTS (
    ID NUMBER AUTOINCREMENT,
    DEPT_NAME VARCHAR(100),
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);
